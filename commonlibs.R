commonlibs <- function(pathlib = "C:/R_Libraries" ) {
  
  print("------------------------------------------------------------------------------------------------------------")
  print(version)
  print("------------------------------------------------------------------------------------------------------------")
  .libPaths(file.path(pathlib, paste0(R.Version()[c("major","minor")], collapse = ".")))
  
  
  print("Libraries in use: ")
  print(.libPaths())
  print("------------------------------------------------------------------------------------------------------------")
  print(sessionInfo())
}
